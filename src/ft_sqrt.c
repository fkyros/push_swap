int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb == 0 || nb == 1)
		return (1);
	else if (nb > 2)
	{
		while (i <= nb / 2)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
